package org.iot.dsa.dslink.jdbc;

import java.sql.Connection;
import java.sql.PreparedStatement;
import java.sql.ResultSet;
import java.sql.SQLException;
import org.iot.dsa.dslink.ActionResults;
import org.iot.dsa.node.action.DSIActionRequest;

/**
 * Represents a prepared statement that can be used for queries.
 *
 * addParameter
 *
 * @author Aaron Hansen
 */
public class JDBCPreparedQuery extends AbstractPreparedStatement {

    ///////////////////////////////////////////////////////////////////////////
    // Class Fields
    ///////////////////////////////////////////////////////////////////////////

    ///////////////////////////////////////////////////////////////////////////
    // Instance Fields
    ///////////////////////////////////////////////////////////////////////////

    ///////////////////////////////////////////////////////////////////////////
    // Constructors
    ///////////////////////////////////////////////////////////////////////////

    ///////////////////////////////////////////////////////////////////////////
    // Public Methods
    ///////////////////////////////////////////////////////////////////////////

    ///////////////////////////////////////////////////////////////////////////
    // Protected Methods
    ///////////////////////////////////////////////////////////////////////////

    @Override
    protected ActionResults getResults(Connection conn,
                                       PreparedStatement stmt,
                                       DSIActionRequest request) {
        ResultSet res = null;
        try {
            res = stmt.executeQuery();
        } catch (SQLException e) {
            cleanClose(res, stmt, conn, this);
            throw new IllegalStateException("Query failed: " + e);
        }
        ActionResults act = null;
        try {
            act = new JDBCTable(request, res, this);
        } catch (SQLException e) {
            cleanClose(res, stmt, conn, this);
            throw new IllegalStateException("Failed to retrieve data from database: " + e);
        }
        return act;
    }

    ///////////////////////////////////////////////////////////////////////////
    // Package Methods
    ///////////////////////////////////////////////////////////////////////////

    ///////////////////////////////////////////////////////////////////////////
    // Private Methods
    ///////////////////////////////////////////////////////////////////////////

    ///////////////////////////////////////////////////////////////////////////
    // Inner Classes
    ///////////////////////////////////////////////////////////////////////////

    ///////////////////////////////////////////////////////////////////////////
    // Initialization
    ///////////////////////////////////////////////////////////////////////////

}
